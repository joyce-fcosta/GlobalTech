    <h2>2. Quais Informações Pessoais coletamos</h2>
    <p>Quando você visita, faz downloads e/ou usa qualquer dos nossos Serviços, podemos coletar
        Informações Pessoais de uso agregadas como a atividade de navegação e transmissão rápida
        de Visitantes e Usuários nos Serviços, mapas de calor e rolagens de sessões, Informações
        Pessoais não identificadoras relativas ao dispositivo do Visitante ou do Usuário, sistema
        operacional, navegador da internet, resolução da tela, idioma e configurações do teclado,
        provedor de serviços de internet, páginas encaminhadoras/de saída, marcações de data/tempo
        etc.
    </p>
    <br>
    <hr>
    <h2>2.1. Informações dos Usuários:</h2>
    <p>Para prestar-lhe os Serviços, temos que coletar Informações Pessoais relativas a uma pessoa
        física identificada ou identificável (“Informações Pessoais”). Coletamos Informações
        Pessoais que você nos envia, do seu uso dos Serviços e de outras fontes. Aqui estão
        alguns tipos de Informações Pessoais que coletamos sobre você:Quando se registra nos nossos Serviços, compra e/ou registra nomes de domínios, usa qualquer dos nossos Serviços e/ou quando entra em contato conosco diretamente
        por qualquer canal de comunicação (como chamados de suporte no Global e e-mails)</p>
    <br>
    <hr>
    <h2>2.2. Informações de Usuários de Usuários</h2>
    <p>Também podemos coletar Informações Pessoais pertinentes a visitantes e usuários dos sites ou serviços dos nossos
        Usuários (“Usuários de Usuários”) exclusivamente pelos nossos Usuários e em seu nome (conforme mais detalhadamente descrito na Seção 6 abaixo).</p>
    <br>
    <hr>
    <h2>3. Como usamos essas Informações Pessoais</h2>
    <p>Usamos as Informações Pessoais que coletamos sobre você para prestar os nossos serviços e torná-los melhores e mais seguros.
        Também coletamos e usamos Informações Pessoais de modo a contatar Usuários e cumprir as leis aplicáveis a nós.</p>
    <br>
    <hr>
    <h2>4. ​Como compartilhamentos as suas Informações Pessoais</h2>
    <p>A global fez parcerias com alguns provedores de serviços selecionados cujos serviços e soluções
        complementam, facilitam e aprimoram os nossos. Eles incluem serviços de hospedagem e
        colocação de servidores, comunicações e redes de apresentação de conteúdo
        (CDNs), serviços de segurança cibernética e de dados, serviços de processamento de
        cobrança e pagamentos, registradores de nomes de domínio, serviços de detecção e prevenção
        de fraudes, analítica da internet, serviços de distribuição e monitoramento de e-mails, serviços
        de gravação e acesso remoto a sessões, serviços de mensuração de desempenho, otimização de dados e marketing, provedores de conteúdos e os nossos consultores jurídicos e financeiros (conjuntamente, “Terceiros Provedores de Serviços”).</p>
    <br>
    <hr>
    <h2>5. Onde armazenamos as suas Informações Pessoais?</h2>
    <p>A Global Tech é responsável pelo tratamento de Informações Pessoais que receba ao amparo da
        Estrutura do Escudo de Privacidade e transfira posteriormente a terceiros atuando
        como representantes em seu nome. A global cumpre os Princípios do Escudo de Privacidade
        com relação a todas as transferências subsequentes de Informações Pessoais da UE, inclusive
        todas as disposições de responsabilidade por transferências subsequentes.</p>
    <br>
    <hr>
    <h2>6. Informações Pessoais de Usuários de Usuários</h2>
    <p>A global pode coletar, armazenar e tratar certas Informações Pessoais de Usuários de Usuários (“Informações de Usuários de Usuários”)
        nicamente em representação dos nossos Usuários e mediante orientação deles. Por exemplo, todos os nossos Usuários podem importar
        os seus contatos de e-mail de serviços de terceiros como o Gmail ou de outro modo coletar e armazenar contatos por meio dos Sites
        de Usuários deles. Esses contatos são então armazenados pela Global, em representação dos Usuários. Para tais propósitos, a atua
        e será considerada um “Tratador” e não o “Controlador” (da forma que ambos os termos com inicial maiúscula estão definidos no Regulamento Geral de Proteção de Dados da União Europeia (“GDPR”)) dessas Informações de Usuários de Usuários.</p>
    <br>
    <hr>
    <h2>7. Uso de cookies e outras tecnologias de terceiros</h2>
    <p>Observe que não alteramos as nossas práticas em resposta a um sinal “Não Rastrear” no cabeçalho HTTP de um navegador
        ou aplicativo móvel. No entanto, a maioria dos navegadores permite que você controle cookies, inclusive aceitá-los ou não e como removê-los. Você configurar a maioria dos navegadores para notificá-lo caso receba um cookie ou pode optar por bloquear cookies no seu navegador.</p>
    <br>
    <hr>
    <h2>8. Comunicações da Global Tech</h2>
    <p>Podemos usar as suas Informações Pessoais para enviar-lhe conteúdo e mensagens promocionais por e-mail,
        mensagens de texto, notificações na nossa plataforma, chamadas de marketing e formas similares de comunicação
        da Global ou dos nossos parceiros (atuando em representação da Global) por tais meios.</p>
