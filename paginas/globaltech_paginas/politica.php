    <hr>
    <h2>9. Os seus direitos com relação às suas Informações Pessoais</h2>
    <p>A Global Tech concorda que é imperativo que você tenha controle sobre as suas Informações Pessoais.
        Ao amparo de regulamentos de privacidade mundialmente, a Global está tomando medidas para habilitá-lo a acessar,
        receber cópias, atualizar, reformular, excluir ou limitar o uso das suas Informações Pessoais.Antes de atender
        à sua solicitação, podemos solicitar que você envie informações adicionais para confirmar a sua identidade e
        para propósitos de segurança. Reservamos o direito de cobrar uma taxa, quando permitido por lei (por exemplo:
        se a sua solicitação for infundada ou excessiva).</p>
