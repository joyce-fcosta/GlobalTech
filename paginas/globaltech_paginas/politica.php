    <p>Os nossos serviços oferecem blogs, comunidades e fóruns de suporte que podem ser acessados
        publicamente. Observe que todas as Informações Pessoais que você envie em qualquer dessas
        áreas poderão ser lidas, coletadas e usadas por terceiros que tenham acesso a tais informações.
        Para solicitar a remoção das suas Informações Pessoais dos nossos blogs, comunidades ou fóruns,
        fique à vontade para entrar em contato conosco pelo: privacy@globaltech.com. Em alguns casos, talvez não consigamos remover as suas Informações Pessoais dessas áreas.</p>
