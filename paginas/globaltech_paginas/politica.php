    <h2>11. Perguntas e reclamações</h2>
    <p>Caso tenha perguntas ou dúvidas sobre a nossa coleta, uso ou divulgação de Informações Pessoais ou acredite que não cumprimos esta Política de Privacidade ou as leis aplicáveis de proteção de dados, entre em contato conosco –
        os nossos dados constam no final desta Política de Privacidade.</p>
    <br>
    <hr>
    <h2>12. Retenção de dados</h2>
    <p>Podemos reter as suas Informações Pessoais (assim como Informações Pessoais de Usuários de Usuários) enquanto a
        sua Conta de Usuário estiver ativa, conforme indicado nesta Política de Privacidade, ou conforme seja necessário
        para prestar a você os nossos Serviços.</p>
    <br>
    <hr>
    <h2>13. Segurança</h2>
    <p>A Global Tech implantou medidas de segurança concebidas para proteger as Informações Pessoais que você compartilhar
        conosco, inclusive medidas físicas, eletrônicas e procedimentais. Entre outras coisas, oferecemos acesso seguro por
        HTTPS à maioria das áreas nos nossos Serviços; a transmissão de informações confidenciais de pagamento (como números de cartões de crédito)
        por meio dos nossos formulários de compras designados está protegida por uma conexão criptografada SSL/TLS conforme os padrões do setor; e mantemos regularmente uma certificação PCI DSS (Normas de Segurança de Dados de do Setor de Cartões de Pagamentos). Também monitoramos regularmente os nossos sistemas quanto possíveis vulnerabilidades e ataques e buscamos regularmente aperfeiçoar ainda mais a segurança dos nossos Serviços e a proteção da privacidade dos nossos Visitantes e Usuários.</p>
    <br>
    <hr>
    <h2>14. Sites de terceiros</h2>
    <p>Os nossos Serviços podem conter links para outros sites ou serviços. Não somos responsáveis pelas práticas de
        privacidade de tais sites ou serviços. Recomendamos que tenha cuidado ao sair dos nossos Serviços e leia as
        declarações de privacidade de todo e qualquer site ou serviço que você visite antes enviar as suas Informações
        Pessoais. Esta Política de Privacidade não se aplica a tais sites e serviços de terceiros linkados.</p>
    <br>
    <hr>
    <h2>15. Candidaturas a cargos na Global Tech </h2>
    <p>Observe que a Global pode reter Informações dos Candidatos apresentadas a ela por no máximo dois anos após o
        preenchimento ou encerramento da vaga correspondente. Isso é feito para que possamos reconsiderar Candidatos
        para outros cargos e oportunidades na Global; para que possamos usar as Informações dos Candidatos como referência
        para candidaturas futuras apresentadas por eles; e caso o Candidato seja contratado, para propósitos adicionais
        de colocação e de negócios relativos ao trabalho dele na tech.</p>
